(function () {
  var ORDER = ["en", "pt", "es", "it", "zh"];
  var NAMES = { en: "EN", pt: "PT", es: "ES", it: "IT", zh: "中文" };
  var FLAGS = { en: "public/flag-us.svg", pt: "public/flag-br.svg", es: "public/flag-es.svg" };
  var LABELS = { en: "English", pt: "Português", es: "Español", it: "Italiano", zh: "中文" };
  var DICT = window.DOG_I18N || {};
  var baseline = {};

  function els() { return document.querySelectorAll("[data-i18n]"); }
  function has(lang) { return lang === "en" || !!DICT[lang]; }

  function apply(lang) {
    var d = DICT[lang] || {};
    els().forEach(function (el) {
      var k = el.getAttribute("data-i18n");
      var v = (lang === "en") ? baseline[k] : (d[k] != null ? d[k] : baseline[k]);
      if (v != null) el.innerHTML = v;
    });
    document.documentElement.setAttribute("lang", lang === "zh" ? "zh-CN" : (lang === "pt" ? "pt-BR" : lang));
    try { sessionStorage.setItem("dogLang", lang); } catch (e) {}
    var sw = document.getElementById("langsw");
    if (sw) sw.querySelectorAll("button").forEach(function (b) {
      b.classList.toggle("active", b.getAttribute("data-lang") === lang);
    });
  }

  function pick() {
    // English on every fresh entry. ?lang= always wins (shared links); a choice made
    // via the switcher persists only for the current session, never across visits.
    try {
      var q = new URLSearchParams(location.search).get("lang");
      if (q && has(q)) return q;
    } catch (e) {}
    try {
      var s = sessionStorage.getItem("dogLang");
      if (s && has(s)) return s;
    } catch (e) {}
    return "en";
  }

  function build() {
    var sw = document.getElementById("langsw");
    if (!sw) return;
    var html = "";
    ORDER.forEach(function (lang) {
      if (!has(lang)) return;
      var label = LABELS[lang] || NAMES[lang];
      html += '<button type="button" class="flag" data-lang="' + lang + '" title="' + label +
        '" aria-label="' + label + '"><img src="' + FLAGS[lang] + '" width="22" height="15" alt="' +
        label + '" loading="lazy" decoding="async"></button>';
    });
    sw.innerHTML = html;
    sw.querySelectorAll("button").forEach(function (b) {
      b.addEventListener("click", function () { apply(b.getAttribute("data-lang")); });
    });
  }

  function init() {
    els().forEach(function (el) {
      var k = el.getAttribute("data-i18n");
      if (!(k in baseline)) baseline[k] = el.innerHTML;
    });
    build();
    apply(pick());
  }

  if (document.readyState === "loading") document.addEventListener("DOMContentLoaded", init);
  else init();
})();

